    // -------------------- Koneksi Database --------------------
    private void koneksiDatabase() {
        try {
            String url = "jdbc:postgresql://localhost:5432/PBOPer5"; // ganti dengan nama database kamu
            String user = "postgres";   // user pgAdmin
            String pass = "fahmud";   // password pgAdmin
            conn = DriverManager.getConnection(url, user, pass);
            System.out.println("Koneksi berhasil!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Koneksi gagal: " + e.getMessage());
        }
    }

    private void tampilData() {
        tabModel = new DefaultTableModel(new Object[]{"NIM", "Nama", "Prodi", "Umur", "Gender"}, 0) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM mahasiswa WHERE nim IS NOT NULL");
            while (rs.next()) {
                Object[] data = {
                    rs.getString("nim"),
                    rs.getString("nama"),
                    rs.getString("prodi"),
                    rs.getInt("umur"),
                    rs.getString("gender")
                };
                // Pastikan tidak ada null yang ditambahkan
                for (int i = 0; i < data.length; i++) {
                    if (data[i] == null) {
                        data[i] = ""; // Ganti null dengan string kosong
                    }
                }
                tabModel.addRow(data);
            }
            tblmahasiswa.setModel(tabModel);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        }
    }

    // -------------------- Insert Data --------------------
    private void insertData() {
        try {
            // TAMBAHKAN KOLOM GENDER DALAM SQL
            String sql = "INSERT INTO mahasiswa (nim, nama, prodi, umur, gender) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtnim.getText());
            pst.setString(2, txtnama.getText());
            pst.setString(3, txtprodi.getText());
            pst.setInt(4, Integer.parseInt(txtumur.getText()));
            pst.setString(5, txtgender.getSelectedItem().toString()); // Ambil dari JComboBox
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            clearInput();
            tampilData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Insert gagal: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Umur harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    // -------------------- Update Data --------------------
    private void updateData() {
        try {
            // TAMBAHKAN KOLOM GENDER
            String sql = "UPDATE mahasiswa SET nama=?, prodi=?, umur=?, gender=? WHERE nim=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtnama.getText());
            pst.setString(2, txtprodi.getText());
            pst.setInt(3, Integer.parseInt(txtumur.getText()));
            pst.setString(4, txtgender.getSelectedItem().toString()); // Kolom gender
            pst.setString(5, txtnim.getText()); // WHERE condition
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            } else {
                JOptionPane.showMessageDialog(this, "Data dengan NIM tersebut tidak ditemukan!");
            }
            clearInput();
            tampilData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Update gagal: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Umur harus berupa angka!");
        }
    }

    // -------------------- Delete Data --------------------
    private void deleteData() {
        try {
            String sql = "DELETE FROM mahasiswa WHERE nim=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtnim.getText());
            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Data dengan NIM tersebut tidak ditemukan!");
            }
            clearInput();
            tampilData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Delete gagal: " + e.getMessage());
        }
    }

    // -------------------- Clear Input --------------------
