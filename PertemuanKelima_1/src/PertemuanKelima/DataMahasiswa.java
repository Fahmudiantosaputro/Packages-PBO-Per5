    private void setupTableSelection() {
        tblmahasiswa.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = tblmahasiswa.getSelectedRow();
                if (row >= 0) {
                    try {
                        // Gunakan method yang aman dari null
                        txtnim.setText(getSafeTableValue(row, 0));
                        txtnama.setText(getSafeTableValue(row, 1));
                        txtprodi.setText(getSafeTableValue(row, 2));
                        txtumur.setText(getSafeTableValue(row, 3));

                        String gender = getSafeTableValue(row, 4);
                        if (gender.equals("Perempuan")) {
                            txtgender.setSelectedIndex(1);
                        } else {
                            txtgender.setSelectedIndex(0); // Default Laki-Laki
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Error membaca data: " + e.getMessage());
                    }
                }
            }
        });
    }

// Method helper untuk menghindari NullPointerException
    private String getSafeTableValue(int row, int column) {
        Object value = tblmahasiswa.getValueAt(row, column);
        return (value != null) ? value.toString() : "";
    }
